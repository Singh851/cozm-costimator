export function getFxToUSD(currencyCode: string): number {
  const match = countries.find(c => c.currency[0] === currencyCode);
  return match?.defaultFxToUSD || 1.0;
}

export function getCountry(code: string): Country | undefined {
  return countries.find(c => c.code === code);
}

export function getCity(countryCode: string, cityCode: string) {
  const country = getCountry(countryCode);
  return country?.cities.find(c => c.code === cityCode);
}

export function getState(countryCode: string, cityCode: string) {
  const country = getCountry(countryCode);
  if (!country?.states) return undefined;
  const city = country.cities.find(c => c.code === cityCode);
  if (!city) return undefined;
  // Map city to state by convention (city code contains state info)
  const stateCode = city.name.match(/,\s*(\w+)$/)?.[1];
  return country.states.find(s => s.code === stateCode || s.name === stateCode);
}
